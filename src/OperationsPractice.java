
public class OperationsPractice 
{

	public static void main(String[] args) 
	{
		int newNumber;
		newNumber = 12;
		System.out.println("Number =: " + newNumber);
		newNumber += 5;
		System.out.println("Add 5: " + newNumber);
		newNumber /= 2;
		System.out.println("Divide by 2: " + newNumber);
		newNumber *= 4;
		System.out.println("Multiply by 4: " + newNumber);
		newNumber -= 6;
		System.out.println("Subtract 6: " + newNumber);
		newNumber %= 2;
		System.out.print("Modualo divide by 2: " + newNumber);
		
	}

}
